class MethodDemo7
{
	public static String display(String name)
	{
		return name;
	}
	public static char display1(char gender)
	{
		return gender;
	}
	public static String display2()
	{
		return "sruthi";
	}
	public static void main(String[] args)
	{
	System.out.println("The name is "+display("Revathi"));
	System.out.println("The gender is "+display1('f'));
	System.out.println("The name is "+display2());
	}


}